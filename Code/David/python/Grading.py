number = input('Enter your numerical score ')
grade = int(number)
if grade <= 53:
    print('F-')
if 53 <= grade <= 58:
    print("F")
if grade == 59:
    print('F+')
if 60 <= grade <= 62:
    print('D-')
if 63 <= grade <= 68:
    print("D")
if grade == 69:
    print('D+')
if 70 <= grade <= 72:
    print('C-')
if 73 <= grade <= 78:
    print("C")
if grade == 79:
    print('C+')
if 80 <= grade <= 82:
    print('B-')
if 83 <= grade <= 88:
    print("B")
if grade == 89:
    print('B+')
if 90 <= grade <= 92:
    print('A-')
if 93 <= grade <= 98:
    print("A")
if grade >= 99:
    print('A+')
